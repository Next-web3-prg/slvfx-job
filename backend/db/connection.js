const { Pool } = require('pg');
require('dotenv').config({ path: require('path').join(__dirname, '../../.env') });

const pool = new Pool({
  connectionString: process.env.DATABASE_URL || `postgresql://${process.env.DB_USER || 'postgres'}:${process.env.DB_PASSWORD || 'password'}@${process.env.DB_HOST || 'localhost'}:${process.env.DB_PORT || 5432}/${process.env.DB_NAME || 'slvfx_job_board'}`,
  ssl: {
    rejectUnauthorized: false,
    require: true
  },
  max: 20, // Maximum number of clients in the pool
  idleTimeoutMillis: 30000,
  connectionTimeoutMillis: 2000,
});

// Test the connection
pool.on('connect', () => {
  console.log('üîó Connected to PostgreSQL database');
});

pool.on('error', (err) => {
  console.error('‚ùå Unexpected error on idle client', err);
  process.exit(-1);
});

async function connectDB() {
  try {
    const client = await pool.connect();
    await client.query('SELECT NOW()');
    client.release();
    return true;
  } catch (error) {
    console.error('‚ùå Database connection failed:', error);
    throw error;
  }
}

async function query(text, params) {
  const start = Date.now();
  try {
    const res = await pool.query(text, params);
    const duration = Date.now() - start;
    console.log('üìä Executed query', { text, duration, rows: res.rowCount });
    return res;
  } catch (error) {
    console.error('‚ùå Query error:', error);
    throw error;
  }
}

module.exports = {
  connectDB,
  query,
  pool
}; 